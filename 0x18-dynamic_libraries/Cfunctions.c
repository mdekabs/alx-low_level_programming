/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 * Return: the addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts b from a
 * @a: firt number
 * @b: second number
 * Return: the subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: the multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides a with b
 * @a: dividend
 * @b: divisor
 * Return: the division result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - remainder after integer division
 * @a: dividend
 * @b: divisor
 * Return: the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
